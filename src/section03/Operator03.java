package section03;
/*
 *  3. 비교 연산자
 *  연산 결과를 비교하여 boolean 으로 변환한다.
 * 	 > : 크다 
 *  >= : 크거나 같다
 *   < : 작다
 *  <= : 작거나 같다
 *	== : 같다
 *	!= : 다르다
 * 
 * 
 */
public class Operator03 {
	public static void main(String[] args) {
		boolean result = 10 < 20;
		System.out.println(result);
		
		if(result) {
			System.out.println("10은 20보다 작다.");
		}  
		else {
				System.out.println("10은 20보다 크다.");
			}
		}
	}





	